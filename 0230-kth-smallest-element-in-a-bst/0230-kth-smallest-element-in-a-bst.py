
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def kthSmallest(self, root: Optional[TreeNode], k: int) -> int:
        st=[]
        cnt=0
        while root or st:
            while root:
                st.append(root)
                root=root.left
            root=st.pop()
            cnt+=1
            if cnt==k:
                return root.val
            root=root.right


        